	<h1 class="p-6">About Us</h1>

	<p>
		It is estimated that there exist between 1.3 to 2 million stray cats in New Jersey alone. This problem is especially prevalent not only in Hoboken but in city communities alike. Stray cats are both a boon and a bane on the communities they inhabit, so it is important to keep track of stray cats for both public health and city maintenance. Current implementations of Trap Neuter Release (TNR) programs for managing cat populations are effective but labor intensive, thus, our project idea aims to reduce their cost and increase efficiency. <br><br>

		We at C.A.T.S. serve to create a camera system which can be deployed at scale to identify cats, specifically feral cats in cities such as Hoboken. These cameras will be easy to install and simple to monitor from a central dashboard. Computer vision will be used to identify cats and their features to provide detailed insights into population and health trends. This project can be easily integrated with city-wide stray cat control programs to immediately assist coordinators and volunteers who are working in large outdoor areas, where it can be difficult to keep track of individual cats. Additionally, these teams can benefit from machine learning algorithms used to monitor the cats' behavior and movements, looking for signs of illness or other problems that may require intervention. Animal control coordinators and volunteers can then ensure that the cats are healthy and well-cared for, and respond quickly to any potential problems.
	</p>

